package classfortests;

import hermes.Hermes;

public class Adress extends Hermes {
    
    private int numero;
    private String rue;

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getRue() {
        return rue;
    }

    public void setRue(String rue) {
        this.rue = rue;
    }
}
